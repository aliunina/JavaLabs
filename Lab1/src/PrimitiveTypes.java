public class PrimitiveTypes {
    public static void main(String args[]) {
        // объявляем различные типы переменных
        // одновременно инициализируем их
        short s = 12; // переменная типа short
        int i = 125; // переменная типа int
        long l = 123567; // переменная типа long
        float f = 123.98f; // переменная типа float
        double d = 234980.567; // переменная типа double
        char c = 'A'; // переменная типа char
        String str = "Hellow";
        boolean b = false;
        System.out.print(" short s = " + s);
        System.out.print(" int i = " + i);
        System.out.print(" long l = " + l);
        System.out.println("float f = " + f);
        System.out.println("double d = " + d);
        System.out.println("char c = " + c);
        System.out.println("String str = " + str);
        System.out.println("boolean b = " + b);
    }
}
